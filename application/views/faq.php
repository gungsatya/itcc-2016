                           <li>
                              <b>Q</b> : <b><i>Bagaimana cara mendaftar ITCC 2016 ?</i></b><br>
                              <b>A</b> : Caranya sangat mudah.
                              <ol>
                                 <li>Daftar pada <a href="<?php echo base_url();?>signup">link</a> ini.</li>
                                 <li>Lengkapi data diri dan/atau data anggota tim.</li>
                                 <li>Bayar lomba melalui ATM pada rekening <b>Bank BRI</b>, <b><i>1095 0100 4980 503 a.n Veranita Lamrotua</i></b>.</li>
                                 <li>Kirim foto bukti pembayaran pada <a href="<?php echo base_url();?>verifikasi">halaman verifikasi</a>. Pastikan kamu sudah masuk(login) ke ITCC 2016.</li>
                                 <li>Tunggu proses verifikasi selesai.</li>
                              </ol>
                              <br>
                           </li>
                           <li>
                              <b>Q</b> : <b><i>Apakah yang dimaksud kartu identitas pada saat pendaftaran?</i></b><br>
                              <b>A</b> : Kartu identitas yang dimaksud untuk kategori Pengembangan Ide Bisnis TIK adalah Kartu Tanda Penduduk atau jika tidak ada bisa menggunakan Kartu Tanda Siswa/Mahasiswa. Sedangkan untuk kategori Pemrograman, Desain Web dan juga Cerdas Cermat kartu identitas yang dimaksud adalah Kartu Tanda Siswa. 
                              <br>
                              <br>
                           </li>
                           <li>
                              <b>Q</b> : <b><i>Dimana lokasi lomba diadakan ?</i></b><br>
                              <b>A</b> : Lokasi lomba akan diadakan di kampus Teknologi Informasi Udayana Bukit, Jimbaran. (<a target="_blank" href="https://www.google.co.id/maps/place/Jalan+Raya+Kampus+Udayana,+Universitas+Udayana-Teknologi+Informasi,+Jimbaran,+Kuta+Sel.,+Kabupaten+Badung,+Bali/@-8.7963957,115.1742693,17z/data=!3m1!4b1!4m5!3m4!1s0x2dd244bd5227dd5b:0x2987c2fa4d855e33!8m2!3d-8.796401!4d115.176458">Google Maps</a>)
                              <br>
                              <br>
                           </li>
                           <li>
                              <b>Q</b> : <b><i>Apakah semua kalangan dapat mengikuti kategori lomba umum ?</i></b><br>
                              <b>A</b> : Pada kategori lomba umum dibatasi dengan usia maksimal 24 tahun.
                              <br>
                              <br>
                           </li>
                           <li>
                              <b>Q</b> : <b><i>Apakah hadiah jalan-jalan di Bali berlaku juga untuk pemenang SMA/SMK ?</i></b><br>
                              <b>A</b> : Hadiah jalan-jalan di Bali hanya berlaku bagi pemenang lomba Pengembangan Ide Bisnis TIK.
                              <br>
                              <br>
                           </li>
                           <li>
                              <b>Q</b> : <b><i>Apakah bisa daftar offline ?</i></b><br>
                              <b>A</b> : Pendaftaran hanya tersedia Online melalui laman <a href="<?php echo base_url();?>">itcc.unud.ac.id</a> .
                              <br>
                              <br>
                           </li>
                           <li>
                              <b>Q</b> : <b><i>Kenapa tidak ada lomba Sistem Informasi ?</i></b><br>
                              <b>A</b> : Hal ini karena ITCC 2016 berharap karya-karya para peserta dapat diimplementasikan langsung ke masyarakat umum untuk menyelamatkan lingkungan, selain juga menyukseskan program <a href="http://1000startupdigital.id/">1000 Startup</a>.
                              <br>
                              <br>
                           </li>
                           <li>
                              <b>Q</b> : <b><i>Berapakah saya harus membayar di ATM ?</i></b><br>
                              <b>A</b> : Nominal pembayaran bisa di lihat di halaman 'Verifikasi'. Nominal pembayaran harus sesuai untuk mempermudah proses.</a>.
                              <br>
                              <br>
                           </li>
                           <li>
                              <b>Q</b> : <b><i>Apa arti dari tema ITCC kali ini ?</i></b><br>
                              <b>A</b> : Melalui ITCC 2016 diharapkan munculnya aplikasi-aplikasi yang kreatif dan invatif dalam mengatasi isu-isu lingkungan yang ada. Misalnya aplikasi yang dapat mengurangi/menghemat penggunaan energi seperti listrik, bahan bakar dan lain sebagainya.</a>.
                              <br>
